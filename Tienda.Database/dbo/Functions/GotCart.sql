----------------------------------- Función Comprobación Carro sin Terminar --------------------------------

CREATE   FUNCTION dbo.GotCart (
	@UserID [int]
		)
	RETURNS [tinyint]
AS
BEGIN
	DECLARE @GotCart [tinyint] = (SELECT COUNT(*) FROM dbo.Carts C WHERE C.UserID = @UserID GROUP BY C.UserID)
	RETURN @GotCart
END
